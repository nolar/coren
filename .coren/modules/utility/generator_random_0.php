class generator_random_0 extends generator_0
{
#
####################################################################################################
####################################################################################################
####################################################################################################
#
protected $alphabet = '0123456789abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ';
protected $minlength = 1;
protected $maxlength = 1;
#
####################################################################################################
#
function __construct ($configs)
{
	parent::__construct($configs);

	if(isset($configs['alphabet' ])) $this->alphabet  =     (string ) $configs['alphabet' ];
	if(isset($configs['minlength'])) $this->minlength = max((integer) $configs['minlength'], 1               );
	if(isset($configs['maxlength'])) $this->maxlength = max((integer) $configs['maxlength'], $this->minlength);
}
#
####################################################################################################
####################################################################################################
####################################################################################################
#
public function make_string ($data)
{
	$result = '';
	$count  = strlen($this->alphabet);
	$length = rand($this->minlength, $this->maxlength);
	for ($index = 0; $index < $length; $index++)
		$result .= $this->alphabet{rand(0, $count-1)};
	return $result;
}

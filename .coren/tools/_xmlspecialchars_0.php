class _xmlspecialchars_0 extends coren_tool
{
#
####################################################################################################
####################################################################################################
####################################################################################################
#
protected static $char_to_entity = array(
	"&"  => "&amp;" ,
	"<"  => "&lt;"  ,
	">"  => "&gt;"  ,
	"\'" => "&apos;",
	"\"" => "&quot;");
#
####################################################################################################
#
public static function xmlspecialchars ($string)
{
	return strtr($string, self::$char_to_entity);
}
#
####################################################################################################
####################################################################################################
####################################################################################################
}
